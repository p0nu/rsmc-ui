function hostFromUrl(url) {
  try {
    const u = new URL(url);
    return u.hostname.replace(/^www\./, "").toLowerCase();
  } catch {
    return "";
  }
}

function matchBrand(url) {
  const host = hostFromUrl(url);
  if (!host) return null;
  for (const b of BRANDS) {
    if (b.keys.some((k) => host === k || host.endsWith(`.${k}`) || host.includes(k))) {
      // Skip any malformed brand entries defensively.
      if (b.path && b.path.length > 10) return b;
    }
  }
  return null;
}

// Deterministic pleasant color from a string (for letter tiles).
function colorFromName(name) {
  let h = 0;
  for (let i = 0; i < name.length; i++) h = (h * 31 + name.charCodeAt(i)) % 360;
  return `hsl(${h} 55% 45%)`;
}

export default function AppIcon({ url, name = "", size = 22 }) {
  const brand = matchBrand(url);
  if (brand) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        aria-hidden="true"
        style={{ display: "block" }}
        fill={brand.color}
      >
        <path d={brand.path} />
      </svg>
    );
  }
  // Letter-tile fallback.
  const letter = (name.trim()[0] || hostFromUrl(url)[0] || "?").toUpperCase();
  const bg = colorFromName(name || url);
  return (
    <span
      aria-hidden="true"
      style={{
        display: "inline-flex",
        alignItems: "center",
        justifyContent: "center",
        width: size,
        height: size,
        borderRadius: 6,
        background: bg,
        color: "#fff",
        fontSize: size * 0.5,
        fontWeight: 700,
        lineHeight: 1,
      }}
    >
      {letter}
    </span>
  );
}
